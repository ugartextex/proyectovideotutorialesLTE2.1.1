                <p> incentivar el aprendizaje autodidacta de estudiantes </p>
                <p>Los videos tutoriales tienen el objetivo de
                    llegar a quien se educa con temáticas de
                    diferentes asignaturas recibiendo paso a
                    paso las instrucciones que el tutor desea
                    transmitir para el logro académico del fin
                    propuesto que es la mejora y la agilización
                    del proceso de aprendizaje convirtiéndolo en
                    un recurso utilizado para enseñar diferentes
                    asignaturas.
                <p>Actualmente los videos tutoriales son de
                    gran difusión gracias a la masificación del
                    Internet, grabados estos se pueden colgar en
                    la web y llegan muy fácilmente a la
                    audiencia objetivo. El video es una
                    tecnología que permite la captura, la
                    grabación, edición, transmisión y
                    reproducción de secuencias de imágenes, lo
                    que hace que grabar videos tutoriales sea en
                    cierta forma una tarea relativamente sencilla,
                    gracias a los adelantos tecnológicos
                    actuales; la elaboración del contenido de los
                    mismos es lo que requiere un esfuerzo
                    mayor pues de él depende que se cumplan
                    los objetivos trazados que son en definitiva
                    la esencia de estos (Cárdenas Martinéz,
                    2013).
                </p>
                <p>De tal suerte que hablar de las NTIC
                    (Nuevas Tecnologías de la Información y la
                    Comunicación) como herramientas de
                    apoyo en la educación es indispensable; el
                    rol que juegan es de suma importancia
                    permitiendo entre otras cosas la influencia
                    positiva en el proceso de enseñanza
                    aprendizaje, combinándolas con las formas
                    clásicas de enseñanza y no como sustitución
                    de ellas, igualmente estás son un gran apoyo
                    para el docente siempre y cuando éste
                    aprenda a utilizarlas correctamente y son, en
                    el caso de los videos, un apoyo en el proceso
                    de aprendizaje de los estudiantes
                    permitiendo como expresa Pompeya López
                    (2008) “una mayor individualización y
                    flexibilización del proceso instructivo
                    adecuándolo a las necesidades particulares
                    de cada alumno” y continua “permiten
                    presentar la información a través de
                    múltiples formas expresivas pudiendo
                    provocar la motivación del alumno y atender
                    a sus diferentes naturalezas cognitivas”.
                </p>

                <p> Los videos tutoriales, objetivo de este
                    proyecto, además de permitir ser una
                    herramienta de apoyo al proceso de
                    enseñanza aprendizaje puede ser utilizados
                    no solo como herramienta aislada sino
                    también como forma combinada junto a la
                    clase presencial, la televisión, el ordenador y
                    todo el software educativo existente
                    actualmente. Estas formas combinadas,
                    como ya se mencionó constituye, El
                    Blended Learning, Los videos tutoriales
                    constituyen, pues, un artefacto de suma
                    importancia en el proceso enseñanza
                    aprendizaje (Bo Tso, 2015). Estos videos
                    tienen la facilidad, hoy día, de llegar de
                    manera pronta, ya que pueden ser
                    almacenados en youtube, gracias al Internet,
                    al usuario objetivo pues este puede hacer
                    uso de ellos a través de un computador o a
                    través de la telefonía móvil de alta
                    penetración en la población.
                </p>
                <p>
                    Un aspecto importante a tener en cuenta con
                    los videos tutoriales es saber si estos son
                    realmente vistos por los estudiantes, para
                    ello existen mecanismos que a los docentes
                    que elaboran los videos les dan la certeza
                    que las lecciones impartidas por este medio
                    cumplen el objetivo de ser instrumentos de
                    apoyo en el proceso de aprendizaje.
                </p>
